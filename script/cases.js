	// продать вещи
	sale() {
		// определяем какие вещи продаем
		let items = document.querySelector('.confirm-sale__items').childNodes;
		if (items.length <= 0) {
			return false;
		} 
		let wpnsForSale = []; // массив вещей для продажи
		// удаляем вещи из инвентаря
		items.forEach(function(item) {
			let id = +item.getAttribute('data-id');
			let inventItem = player.inventory.find(wpn => wpn.id == id);
			if (inventItem === undefined) return false;
			let wpn = wpnsForSale.find(item => item.id == id);
			// если в массиве есть предмет
			if (wpn !== undefined) {
				wpn.value += 1;
				inventItem.value -= 1;
			} else {
				let weapon = {...player.inventory.find(wpn => wpn.id == id)};
				weapon.value = 1;
				inventItem.value -= 1;
				wpnsForSale.push(weapon);
			}
			if (inventItem.value <= 0) {
				let index = player.inventory.findIndex(wpn => wpn.id == id);
				player.inventory.splice(index, 1);
			}
		})
		visual.setMaxItems();
		visual.input_close('.main__input-sale');
		player.calcCapitalAndItems();
		// добавляем стоимость всех вещей в баланс
		wpnsForSale.forEach(item => {
			cases.balance = Math.round(cases.balance * 10 + item.count * 10 * item.value) / 10;
		})
		visual.print_balance();
	},

